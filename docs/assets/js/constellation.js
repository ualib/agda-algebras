/* Module-dependency constellation (ADR-007, M10 #4).
   Renders docs/assets/constellation.json (generated by mkdocs_gen_library.py)
   as a d3 force-directed graph: one node per module, sized by how many modules
   import it, coloured by top-level tree; one edge per internal import. */
(function () {
  var el = document.getElementById("constellation");
  if (!el || !window.d3) return;
  var d3 = window.d3;

  var GROUP_COLORS = {
    Overture: "#e0a458", Setoid: "#46c7b8", Classical: "#e0815a",
    Order: "#b48ce0", Examples: "#84c97f", Exercises: "#e88bbf",
    Demos: "#6db3e0", Legacy: "#8c968f",
  };
  var color = function (g) { return GROUP_COLORS[g] || "#cfd8d2"; };

  var width = el.clientWidth || 960;
  var height = el.clientHeight || 640;

  var svg = d3.select(el).append("svg")
    .attr("viewBox", [0, 0, width, height])
    .attr("width", "100%").attr("height", height)
    .style("cursor", "grab");
  var root = svg.append("g");
  svg.call(d3.zoom().scaleExtent([0.2, 6]).on("zoom", function (e) {
    root.attr("transform", e.transform);
  }));

  // Soft "star glow" applied to the whole node layer.
  var glow = svg.append("defs").append("filter").attr("id", "star-glow")
    .attr("x", "-60%").attr("y", "-60%").attr("width", "220%").attr("height", "220%");
  glow.append("feGaussianBlur").attr("stdDeviation", 2.1).attr("result", "b");
  var merge = glow.append("feMerge");
  merge.append("feMergeNode").attr("in", "b");
  merge.append("feMergeNode").attr("in", "SourceGraphic");

  // A readout panel so a hovered module's full name is always legible.
  var readout = d3.select(el).append("div").attr("class", "constellation-readout");

  d3.json("/assets/constellation.json").then(function (data) {
    var radius = function (d) { return 3 + Math.sqrt(d.deg) * 1.7; };

    var sim = d3.forceSimulation(data.nodes)
      .force("link", d3.forceLink(data.links).id(function (d) { return d.id; })
        .distance(36).strength(0.22))
      .force("charge", d3.forceManyBody().strength(-60))
      .force("center", d3.forceCenter(width / 2, height / 2))
      .force("collide", d3.forceCollide().radius(function (d) { return radius(d) + 2; }));

    var link = root.append("g")
      .attr("stroke", "#62756b").attr("stroke-opacity", 0.25)
      .selectAll("line").data(data.links).join("line").attr("stroke-width", 0.7);

    var node = root.append("g").attr("filter", "url(#star-glow)")
      .selectAll("circle").data(data.nodes).join("circle")
      .attr("r", radius)
      .attr("fill", function (d) { return color(d.group); })
      .attr("stroke", "#0d1310").attr("stroke-width", 0.6)
      .style("cursor", "pointer")
      .on("click", function (e, d) { window.location.href = d.url; });
    node.append("title").text(function (d) {
      return d.id + " — " + d.deg + " dependent" + (d.deg === 1 ? "" : "s");
    });

    var label = root.append("g").attr("pointer-events", "none")
      .selectAll("text").data(data.nodes).join("text")
      .text(function (d) { return d.id; })
      .attr("font-size", 9).attr("fill", "#dfe5e0")
      .attr("paint-order", "stroke").attr("stroke", "#11160f").attr("stroke-width", 2)
      .attr("dx", function (d) { return radius(d) + 2; }).attr("dy", 3)
      .style("opacity", function (d) { return d.deg >= 14 ? 0.92 : 0; });

    var nbr = new Map(data.nodes.map(function (n) { return [n.id, new Set()]; }));
    data.links.forEach(function (l) {
      var s = l.source.id || l.source, t = l.target.id || l.target;
      nbr.get(s).add(t); nbr.get(t).add(s);
    });
    node.on("mouseenter", function (e, d) {
      var nb = nbr.get(d.id);
      node.attr("opacity", function (o) { return (o.id === d.id || nb.has(o.id)) ? 1 : 0.12; });
      link.attr("stroke-opacity", function (l) {
        return (l.source.id === d.id || l.target.id === d.id) ? 0.85 : 0.03;
      });
      label.style("opacity", function (o) {
        return (o.id === d.id || nb.has(o.id)) ? 1 : (o.deg >= 14 ? 0.3 : 0);
      });
      readout.html("<b>" + d.id + "</b><span>" + d.group + " · imported by " +
        d.deg + " module" + (d.deg === 1 ? "" : "s") + "</span>").style("opacity", 1);
    }).on("mouseleave", function () {
      node.attr("opacity", 1);
      link.attr("stroke-opacity", 0.25);
      label.style("opacity", function (o) { return o.deg >= 14 ? 0.92 : 0; });
      readout.style("opacity", 0);
    });

    node.call(d3.drag()
      .on("start", function (e, d) { if (!e.active) sim.alphaTarget(0.3).restart(); d.fx = d.x; d.fy = d.y; })
      .on("drag", function (e, d) { d.fx = e.x; d.fy = e.y; })
      .on("end", function (e, d) { if (!e.active) sim.alphaTarget(0); d.fx = null; d.fy = null; }));

    sim.on("tick", function () {
      link.attr("x1", function (l) { return l.source.x; })
          .attr("y1", function (l) { return l.source.y; })
          .attr("x2", function (l) { return l.target.x; })
          .attr("y2", function (l) { return l.target.y; });
      node.attr("cx", function (d) { return d.x; }).attr("cy", function (d) { return d.y; });
      label.attr("x", function (d) { return d.x; }).attr("y", function (d) { return d.y; });
    });
  });
})();
